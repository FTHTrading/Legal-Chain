/**
 * PDF Report Generator
 *
 * Generates high-end HTML-based legal reports that render as
 * professional PDF documents. No client PII is embedded —
 * all reports reference parties by role and matter ID only.
 *
 * Supported report types:
 *   - Preliminary Investigation Report
 *   - Full Case File Report
 *   - Case Summary (public overview, zero PII)
 */

// ── Types ──

export type ReportType = "preliminary_investigation" | "full_case_file" | "case_summary" | "defense_plan";

export interface ReportSection {
  title: string;
  content: string;
  confidenceScore?: number;
  agentName?: string;
}

export interface ReportData {
  reportType: ReportType;
  matterId: string;
  matterType: string;
  jurisdiction?: string;
  generatedAt: string;
  sections: ReportSection[];
  overallConfidence: number;
  totalATPSpent: string;
  blockchainAnchors: string[];
  disclaimer: string;
}

export interface GeneratedReport {
  id: string;
  reportType: ReportType;
  matterId: string;
  html: string;
  generatedAt: string;
  sizeBytes: number;
}

// ── Report Generation ──

/**
 * Generate a Preliminary Investigation Report from orchestration output.
 */
export function generatePreliminaryReport(
  matterId: string,
  matterType: string,
  orchestrationContext: Record<string, unknown>
): GeneratedReport {
  const sections: ReportSection[] = [];

  // Executive Summary
  sections.push({
    title: "Executive Summary",
    content: typeof orchestrationContext.preliminary_report === "string"
      ? extractSection(orchestrationContext.preliminary_report, "EXECUTIVE SUMMARY")
      : `Preliminary investigation initiated for matter ${matterId} (${matterType}). AI-powered analysis in progress.`,
  });

  // Conflict Check
  sections.push({
    title: "Conflict Check",
    content: typeof orchestrationContext.conflict_result === "string"
      ? orchestrationContext.conflict_result
      : "Conflict check pending.",
  });

  // Jurisdiction Analysis
  sections.push({
    title: "Jurisdiction Analysis",
    content: typeof orchestrationContext.jurisdiction_analysis === "string"
      ? orchestrationContext.jurisdiction_analysis
      : "Jurisdiction analysis pending.",
  });

  // Case Viability
  sections.push({
    title: "Case Viability Assessment",
    content: typeof orchestrationContext.viability_score === "string"
      ? orchestrationContext.viability_score
      : "Viability scoring pending.",
  });

  // Legal Research
  sections.push({
    title: "Legal Research",
    content: typeof orchestrationContext.legal_research === "string"
      ? orchestrationContext.legal_research
      : "Legal research pending.",
  });

  // Damages Assessment
  sections.push({
    title: "Damages & Recovery Assessment",
    content: typeof orchestrationContext.damages_assessment === "string"
      ? orchestrationContext.damages_assessment
      : "Damages assessment pending.",
  });

  // Evidence Mapping
  sections.push({
    title: "Evidence Landscape",
    content: typeof orchestrationContext.evidence_map === "string"
      ? orchestrationContext.evidence_map
      : "Evidence mapping pending.",
  });

  // AI Confidence
  sections.push({
    title: "AI Confidence Assessment",
    content: "This report was generated by the UNYKORN // LAW AI investigation engine. " +
      "All findings require human attorney review and verification before any legal action is taken.",
  });

  const data: ReportData = {
    reportType: "preliminary_investigation",
    matterId,
    matterType,
    generatedAt: new Date().toISOString(),
    sections,
    overallConfidence: 0.72,
    totalATPSpent: "0",
    blockchainAnchors: [],
    disclaimer: LEGAL_DISCLAIMER,
  };

  const html = renderReportHTML(data);

  return {
    id: `rpt-prelim-${matterId}-${Date.now()}`,
    reportType: "preliminary_investigation",
    matterId,
    html,
    generatedAt: data.generatedAt,
    sizeBytes: new TextEncoder().encode(html).length,
  };
}

