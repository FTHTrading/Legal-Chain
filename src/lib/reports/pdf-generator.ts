// ── Helpers ──

function escapeHTML(str: string): string {
  return str
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

function extractSection(text: string, sectionName: string): string {
  const regex = new RegExp(`${sectionName}[:\\s—-]*\\n([\\s\\S]*?)(?=\\n\\d+\\.|$)`, "i");
  const match = text.match(regex);
  return match ? match[1].trim() : text.slice(0, 500);
}

function extractOrDefault(
  ctx: Record<string, unknown>,
  key: string,
  section: string,
  fallback: string
): string {
  const val = ctx[key];
  if (typeof val !== "string") return fallback;
  if (section) return extractSection(val, section) || val.slice(0, 800);
  return val.slice(0, 800);
}

